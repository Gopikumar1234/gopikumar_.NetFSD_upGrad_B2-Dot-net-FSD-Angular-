let num = 7;

// Positive or Negative using ternary
let result = (num >= 0) ? "Positive" : "Negative";
console.log("Number is: " + result);

// Even or Odd using if-else
if (num % 2 === 0) {
  console.log("Even Number");
} else {
  console.log("Odd Number");
}

// Loop from 1 to num
for (let i = 1; i <= num; i++) {
  console.log(i);
}
